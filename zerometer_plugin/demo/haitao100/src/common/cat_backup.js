	//加载数据
	this.load_cat_data = function(parent_id,cat_level){
		if(this.cat_obj.hasOwnProperty(parent_id)){
			var cat_arr = this.cat_obj[parent_id];
			var option_html = '';
			for(var i=0,len=cat_arr.length;i<len;i++){
				var cat_id = cat_arr[i].cat_id;
				var cat_name = cat_arr[i].cat_name;
				var tax_type = cat_arr[i].tax_type;
				var dutiable_value = cat_arr[i].dutiable_value;
				var tax_rate = cat_arr[i].tax_rate;
				option_html += '<option value="'+cat_id+'" tax_type="'+tax_type+'" dutiable_value='+dutiable_value+' tax_rate='+tax_rate+'>'+cat_name+'</option>';
			}
			var select_obj = $('<select id="'+PluginConf.css_refix+'cat'+cat_level+'" cat_level="'+cat_level+'"><option value="-1">请选择</option>'+option_html+'</select>');
			select_obj.change(function(){
				var parent_id = $(this).val();
				var cat_level = parseInt($(this).attr('cat_level'));
				if(parent_id=='-1'){
					widget_ob.find('select:gt('+cat_level+')').remove();
				}else{
					_this.load_cat_data(parent_id,cat_level+1);
				}
			});
			if(cat_level==0){
				widget_ob.empty();
			}else{
				widget_ob.find('select:gt('+(cat_level-1)+')').remove();
			}
			widget_ob.append(select_obj);
		}
	}
	//生成控件
	_this.load_cat_data('00',0);
	
	//返回操作的jquery对象
	return widget_ob;
